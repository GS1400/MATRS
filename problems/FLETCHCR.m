% FLETCHCR function
% 
% input
%   .x  % point
%   .n  % problem dimension
%
% output
%  .f  % function value at x
%
function f=FLETCHCR(x,n)
f=0;
for i=1:n-1
    A=x(i)*(x(i)+1)-x(i+1)-1; f=f+100*A^2;
end
