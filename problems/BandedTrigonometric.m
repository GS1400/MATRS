% Bandedtrigonometric function
% 
% input
%   .x  % point
%   .n  % problem dimension
%
% output
%  .f  % function value at x
%
function f=BandedTrigonometric(x,n)
f=0;
for i=1:n
    A = i*(1-cos(x(i)));
   if i>1, A=A+i*sin(x(i-1));end
   if i<n, A=A-i*sin(x(i+1));end
   f=f+A;
end   
